//how to insert/exchange data using dot notation

var point = {
    x: 10,
    y: 20,
    z: 30
}
point.x = 40
point.a = 50

console.log(point)


//how to insert/exchange data using array notation

var point1 = {
    x: 10,
    y: 20,
    z: 30
}
point1['z'] = 100

point1['a'] = 50

console.log(point1)